package edu.mayo.bior.pipeline.SNPEff;

public class SNPEffectColInfo {
	
   String effect; 
   String effect_impact; 
   String functional_class;
   String codon_change;
   String amino_acid_change;
   String gene_name;
   String gene_bioType;
   String coding;
   String transcript;
   String  exon;
   
   
public String getEffect() {
	return effect;
}
public void setEffect(String effect) {
	this.effect = effect;
}
public String getEffect_impact() {
	return effect_impact;
}
public void setEffect_impact(String effect_impact) {
	this.effect_impact = effect_impact;
}
public String getFunctional_class() {
	return functional_class;
}
public void setFunctional_class(String functional_class) {
	this.functional_class = functional_class;
}
public String getCodon_change() {
	return codon_change;
}
public void setCodon_change(String codon_change) {
	this.codon_change = codon_change;
}
public String getAmino_acid_change() {
	return amino_acid_change;
}
public void setAmino_acid_change(String amino_acid_change) {
	this.amino_acid_change = amino_acid_change;
}
public String getGene_name() {
	return gene_name;
}
public void setGene_name(String gene_name) {
	this.gene_name = gene_name;
}
public String getGene_bioType() {
	return gene_bioType;
}
public void setGene_bioType(String gene_bioType) {
	this.gene_bioType = gene_bioType;
}
public String getCoding() {
	return coding;
}
public void setCoding(String coding) {
	this.coding = coding;
}
public String getTranscript() {
	return transcript;
}
public void setTranscript(String transcript) {
	this.transcript = transcript;
}
public String getExon() {
	return exon;
}
public void setExon(String exon) {
	this.exon = exon;
}

}
